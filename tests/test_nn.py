@pytest.mark.task4_3
@given(tensors(shape=(1, 1, 4, 4)))
def test_avg(t: Tensor) -> None:
    out = minitorch.avgpool2d(t, (2, 2))
    assert_close(
        out[0, 0, 0, 0], sum([t[0, 0, i, j] for i in range(2) for j in range(2)]) / 4.0
    )

    out = minitorch.avgpool2d(t, (2, 1))
    assert_close(
        out[0, 0, 0, 0], sum([t[0, 0, i, j] for i in range(2) for j in range(1)]) / 2.0
    )

    out = minitorch.avgpool2d(t, (1, 2))
    assert_close(
        out[0, 0, 0, 0], sum([t[0, 0, i, j] for i in range(1) for j in range(2)]) / 2.0
    )
    minitorch.grad_check(lambda t: minitorch.avgpool2d(t, (2, 2)), t)


@pytest.mark.task4_4
@given(tensors(shape=(2, 3, 4)))
def test_max(t: Tensor) -> None:
    ## check reduce over all for max
    # shape (3, 2)
    t = tensor([[2, 3], [4, 6], [5, 7]])

    # reduce all dims, (3 -> 1, 2 -> 1)
    t_max_all = t.max()

    # shape (1, 1)
    t_max_all_expected = tensor([7])

    assert_close(t_max_all[0], t_max_all_expected[0])

    # check reduction along one dim
    # shape (3, 2)
    t = tensor([[1, 2, 3], [1, 4, 6], [1, 5, 7]])

    # here 1 means reduce the 1st dim, 2 -> nothing
    t_max_2 = t.max(1)

    # shape (3)
    t_max_2_expected = tensor([[3], [6], [7]])
    assert t_max_2.is_close(t_max_2_expected).all().item()


